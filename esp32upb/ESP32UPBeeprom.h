void reseteeprom(uint32_t device_vid_pid)
{
	uint8_t eepromdata[256];
	
	switch (device_vid_pid)
	{
		case 0x00040022: // US140T
			memcpy(eepromdata,eepromdataUS140T,256);
			break;
			
		case 0x0004003e: // US2240T
			memcpy(eepromdata,eepromdataUS2240T,256);
			break;
		default:
			Serial.printf("Error: unknown device_vid_pid of %08x\n",device_vid_pid);
			return;
			break;

	}

  // byte FF is checksum of the 00-FE for validity check

	// use first four bytes of MAC address as serial number
	for (uint8_t i=0; i<4; i++)
	{
		eepromdata[0x0C+i]=hexcharstobyte(WiFi.macAddress()[3*i], WiFi.macAddress()[3*i+1]);
	}

	// mask high bit of first byte to ensure serial number appears positive in UPStart
	eepromdata[0x0C] = eepromdata[0x0C] & 0x7F;

  for (uint8_t i=0; i<0xFF; i++)
  {
    EEPROM.write(i,eepromdata[i]);
  }

  Serial.printf("Checksum %02X\n",geteepromchecksum());

  seteepromchecksum();

	EEPROM.commit();
}

