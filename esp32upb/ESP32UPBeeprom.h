// checksum the first 192 registers
// ie don't include the scratch and status registers
// and FF where the checksum is stored

uint8_t geteepromchecksum()
{
  uint8_t checksum=0;
  for (uint8_t i=0; i<0xC0; i++)
  {
    // cheating as uint8_t won't ever go over FF. we hope.
    if (i != 0xF9) {checksum+=EEPROM.read(i);}
	}

  return checksum;
}

