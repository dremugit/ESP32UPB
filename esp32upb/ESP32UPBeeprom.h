// checksum the first 192 registers
// ie don't include the scratch and status registers
// and FF where the checksum is stored

void seteepromchecksum()
{
	// use first four bytes of MAC address as serial number
	for (uint8_t i=0; i<4; i++)
	{
		EEPROM.write(0x0C+i,hexcharstobyte(WiFi.macAddress()[3*i], WiFi.macAddress()[3*i+1]));
	}

	// mask high bit of first byte to ensure serial number appears positive in UPStart
	EEPROM.write(0x0C, EEPROM.read(0x0C) & 0x7F);

	
  uint8_t checksum=0;
  for (uint8_t i=0; i<0xC0; i++)
  {
    // cheating as uint8_t won't ever go over FF. we hope.
    if (i != 0xF9) {checksum+=EEPROM.read(i);}
  }

  EEPROM.write(0xFF, checksum);
	#if (DEBUG_LEVEL > 1)
	 Serial.printf("Writing checksum %02X\n",checksum);
  #endif
  commitflag=true;
}

