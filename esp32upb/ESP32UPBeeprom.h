void dumpeeprom()
{
  char tempstring[100];

  Serial.print("  //  ");
  for (uint8_t j=0; j<16; j++)
  {
    Serial.printf("%02X   ",j);
  }
  Serial.println();

  for (uint16_t i=0; i<0x200; i=i+16)
  {
    Serial.print("  ");
    for (uint8_t j=0; j<16; j++)
    {
      Serial.printf("0x%02X,",EEPROM.read(i+j));
    }

    Serial.print(" // ");
    Serial.printf("%02X-%02X\n",i,i+15);

    Serial.print("  // ");
    for (uint8_t j=0; j<16; j++)
    {
      uint8_t z=EEPROM.read(i+j);
  
      // blank out unprintable characters to period
      if (isprintable(z))
      {
        Serial.printf("%c",z);
      }
      else
      {
        Serial.print(".");
      }
      Serial.print("    ");
    }
    Serial.println();
  }
}

